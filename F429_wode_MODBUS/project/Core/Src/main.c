//发送一帧数据并接收一帧数据 //如果发送的是多个线圈，记得count*8 线圈只能以1个字节的形式发送
//fx:帧指针
u8 modbus_send_frame(mb_frameHandle *fx1,mb_frameHandle *fx2)
{
    m_result res;
    u16 data[1] = {0x0001};
    
    fx1->address = 1;
    fx1->count = 0x03;
    fx1->regAdds = 0x0000;
    fx1->data = data;
    mb_framePackSend(fx1);
    for(u8 i = 0; i<10; i++)
    {
      if(m_ctrl_dev.frameok ==1)
      {
        res = mb_unpack_frame(fx2);
         if(res == MR_OK)
         {
            return 1;
         }
      }
      HAL_Delay(10);
    }
    return 0;
}




